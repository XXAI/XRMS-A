        $parametros = $request->all();

        if(isset($parametros['region_id'])){
            $municipios = Municipio::where('region_id',$parametros['region_id'])->orderBy('nombre')->get();
        }else{
            $municipios = Municipio::orderBy('nombre')->get();
        }

        return response()->json(['datos'=>$municipios], HttpResponse::HTTP_OK);
